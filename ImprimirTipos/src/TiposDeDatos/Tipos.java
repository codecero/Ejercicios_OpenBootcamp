package TiposDeDatos;

public class Tipos{
    public static void main(String[] args) {


        //mostrar tipos de datos por consola
        byte datoByte = 127;
        System.out.println("Valor de dato Byte: " + datoByte);
        short datoShort = 32767;
        System.out.println("Valor de dato Short: " + datoShort);
        int datoInt = 2147483647;
        System.out.println("Valor de dato Int: " + datoInt);
        long datoLong = 3000000000L;
        System.out.println("Valor de dato Long: " + datoLong);
        float datoFloat =0.15f;
        System.out.println("Valor de dato Float: " + datoFloat);
        double datoDouble =0.45454d;
        System.out.println("Valor de dato Double: " + datoDouble);
        char datoChar ='H';
        System.out.println("Valor de dato Char: " + datoChar);
        String datoString ="Hola mundo";
        System.out.println("Valor de dato String"+ datoString);
        boolean datoBoolean = true;
        System.out.println("Valor de dato Byte"+ datoBoolean);
    }
}

